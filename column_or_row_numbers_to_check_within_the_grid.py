def column_or_row_numbers_to_check_within_the_grid(column_or_row):
    if column_or_row == 0:
        return [1, 2]
    if column_or_row == 1:
        return [0, 2]
    elif column_or_row == 2:
        return [0, 1]
    elif column_or_row == 3:
        return [1, 2]
    elif column_or_row == 4:
        return [0, 2]
    elif column_or_row == 5:
        return [0, 1]
    elif column_or_row == 6:
        return [1, 2]
    elif column_or_row == 7:
        return [0, 2]
    elif column_or_row == 8:
        return [0, 1]
